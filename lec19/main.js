// ==================== عنوان الدرس  =========================

let test;

test = [1, 2, 3, 4, 5];

// اول ميزة الحذف
test.splice(0, 2);

console.log(test); //(3) [3, 4, 5]

// حذف مع اضافة
test.splice(1, 1, 'سجاد'); //(3) [3, 'سجاد', 5]
console.log(test);

// اذا تريد فقط اضافة بدون حذف
test.splice(0, 0, 'سلام'); //(4) ['سلام', 3, 'سجاد', 5]
console.log(test);

// اذا تريد تاخذ قطعه من المصفوفة
console.log(test.slice(0, 3)); //(3) ['سلام', 3, 'سجاد']

// ميزة التقطيع ماتاثر على المصفوفة
